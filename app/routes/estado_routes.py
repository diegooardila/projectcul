from fastapi import APIRouter, HTTPException
from controllers.estado_controller import *
from models.estado_model import Estado

router = APIRouter()

nuevo_estado = EstadoController()


@router.post("/create_estado/")
async def create_estado(estado: Estado):
    rpta = nuevo_estado.create_estado(estado)
    return rpta


@router.get("/get_estado/{id_estado}",response_model=Estado)
async def get_estado(id_estado: int):
    rpta = nuevo_estado.get_estado(id_estado)
    return rpta

@router.get("/get_estados")
async def get_estados():
    rpta = nuevo_estado.get_estados()
    return rpta

@router.put("/update_estado/{id_estado}")
async def update_estado(id_estado: int, estado: Estado):
    rpta = nuevo_estado.update_estado(id_estado, estado)
    return rpta